def call_gemini(prompt: str, model: str, temperature: float) -> str:
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        raise RuntimeError("GEMINI_API_KEY is not set")

    url = f"https://generativelanguage.googleapis.com/v1beta/models/{model}:generateContent?key={api_key}"
    payload = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {"temperature": temperature},
    }
    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(url, data=data, headers={"Content-Type": "application/json"})
    with urllib.request.urlopen(req, timeout=120) as resp:
        raw = json.loads(resp.read().decode("utf-8"))
    gemini_usage = raw.get("usageMetadata", {})
    _capture_usage(
        {
            "prompt_tokens": gemini_usage.get("promptTokenCount"),
            "completion_tokens": gemini_usage.get("candidatesTokenCount"),
            "total_tokens": gemini_usage.get("totalTokenCount"),
        }
    )
    return raw["candidates"][0]["content"]["parts"][0]["text"]


def call_openai(prompt: str, model: str, temperature: float) -> str:
    api_key = os.environ.get("OPENAI_API_KEY")
    if not api_key:
        raise RuntimeError("OPENAI_API_KEY is not set")

    url = "https://api.openai.com/v1/responses"
    payload = {
        "model": model or os.environ.get("OPENAI_MODEL", "gpt-4.1-mini"),
        "input": prompt,
        "temperature": temperature,
    }
    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        url,
        data=data,
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {api_key}",
        },
    )
    with urllib.request.urlopen(req, timeout=180) as resp:
        raw = json.loads(resp.read().decode("utf-8"))
    _capture_usage(raw.get("usage"))

    if raw.get("output_text"):
        return raw["output_text"]

    chunks = []
    for item in raw.get("output", []):
        for content in item.get("content", []):
            if content.get("type") in {"output_text", "text"} and "text" in content:
                chunks.append(content["text"])
    if chunks:
        return "\n".join(chunks)

    return json.dumps(raw, ensure_ascii=False, indent=2)


def call_deepseek(prompt: str, model: str, temperature: float) -> str:
    api_key = os.environ.get("DEEPSEEK_API_KEY")
    if not api_key:
        raise RuntimeError("DEEPSEEK_API_KEY is not set")

    thinking = os.environ.get("DEEPSEEK_THINKING", "disabled").strip().lower()
    payload = {
        "model": model or os.environ.get("DEEPSEEK_MODEL", "deepseek-v4-pro"),
        "messages": [
            {
                "role": "system",
                "content": "You are a careful Linux kernel program repair assistant. Return strict JSON only.",
            },
            {"role": "user", "content": prompt},
        ],
        "temperature": temperature,
        "stream": False,
    }

    if thinking in {"enabled", "disabled"}:
        payload["thinking"] = {"type": thinking}

    try:
        import requests

        response = requests.post(
            "https://api.deepseek.com/chat/completions",
            headers={
                "Content-Type": "application/json",
                "Authorization": f"Bearer {api_key}",
            },
            json=payload,
            timeout=240,
        )
        response.raise_for_status()
        raw = response.json()
        _capture_usage(raw.get("usage"))
        return raw["choices"][0]["message"].get("content", "")
    except ImportError:
        pass
    except Exception as exc:
        if "SSL" not in str(exc) and "_ssl" not in str(exc):
            raise

    # The bundled Python in this workspace may lack _ssl support on Windows.
    # Use PowerShell for DeepSeek HTTPS transport while keeping the API key in
    # environment variables instead of command arguments or files.
    with tempfile.NamedTemporaryFile("w", encoding="utf-8", suffix=".json", delete=False) as f:
        json.dump(payload, f, ensure_ascii=False)
        payload_path = f.name

    ps_script = (
        "Remove-Item Env:HTTP_PROXY,Env:HTTPS_PROXY,Env:ALL_PROXY,Env:GIT_HTTP_PROXY,Env:GIT_HTTPS_PROXY "
        "-ErrorAction SilentlyContinue; "
        "$body = Get-Content -LiteralPath $env:DEEPSEEK_PAYLOAD_FILE -Raw; "
        "$headers = @{ Authorization = ('Bearer ' + $env:DEEPSEEK_API_KEY); 'Content-Type' = 'application/json' }; "
        "$r = Invoke-RestMethod -Uri 'https://api.deepseek.com/chat/completions' -Method Post -Headers $headers -Body $body; "
        "$r | ConvertTo-Json -Depth 100"
    )
    env = os.environ.copy()
    env["DEEPSEEK_PAYLOAD_FILE"] = payload_path
    try:
        completed = subprocess.run(
            ["powershell", "-NoProfile", "-Command", ps_script],
            check=True,
            capture_output=True,
            text=True,
            encoding="utf-8",
            env=env,
            timeout=240,
        )
        raw = json.loads(completed.stdout)
    finally:
        try:
            os.unlink(payload_path)
        except OSError:
            pass

    _capture_usage(raw.get("usage"))
    return raw["choices"][0]["message"].get("content", "")


def call_llm(prompt: str, provider: str = "mock", model: str = "gemini-1.5-pro", temperature: float = 0.2) -> str:
    global _LAST_PROVIDER_USAGE
    _LAST_PROVIDER_USAGE = {}
    started_at = time.monotonic()
    if provider == "mock":
        result = mock_response(prompt)
        _set_call_metadata(provider, model, started_at, {"prompt_tokens": 0, "completion_tokens": 0, "total_tokens": 0})
        return result
    if provider == "gemini":
        result = call_gemini(prompt, model, temperature)
        _set_call_metadata(provider, model, started_at, _LAST_PROVIDER_USAGE)
        return result
    if provider == "openai":
        result = call_openai(prompt, model, temperature)
        _set_call_metadata(provider, model, started_at, _LAST_PROVIDER_USAGE)
        return result
    if provider == "deepseek":
        result = call_deepseek(prompt, model, temperature)
        _set_call_metadata(provider, model, started_at, _LAST_PROVIDER_USAGE)
        return result
    raise ValueError(f"Unsupported provider: {provider}")
