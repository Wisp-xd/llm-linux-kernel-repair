from __future__ import annotations

import json
import os
import re
import subprocess
import tempfile
import time
import urllib.request
from typing import Any, Dict


_LAST_CALL_METADATA: Dict[str, Any] = {}
_LAST_PROVIDER_USAGE: Dict[str, Any] = {}


def _capture_usage(usage: Dict[str, Any] | None) -> None:
    global _LAST_PROVIDER_USAGE
    _LAST_PROVIDER_USAGE = dict(usage or {})


def _set_call_metadata(provider: str, model: str, started_at: float, usage: Dict[str, Any] | None = None) -> None:
    global _LAST_CALL_METADATA
    usage = usage or {}
    _LAST_CALL_METADATA = {
        "provider": provider,
        "model": model,
        "duration_seconds": round(time.monotonic() - started_at, 3),
        "prompt_tokens": usage.get("prompt_tokens", usage.get("input_tokens")),
        "completion_tokens": usage.get("completion_tokens", usage.get("output_tokens")),
        "total_tokens": usage.get("total_tokens"),
    }


def get_last_call_metadata() -> Dict[str, Any]:
    """Return metadata for the most recent call in this process."""
    return dict(_LAST_CALL_METADATA)


def extract_json(text: str) -> Dict[str, Any]:
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    fenced = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", text, re.S)
    if fenced:
        return json.loads(fenced.group(1))

    first = text.find("{")
    last = text.rfind("}")
    if first >= 0 and last > first:
        return json.loads(text[first : last + 1])

    raise ValueError("No JSON object found in model output")


